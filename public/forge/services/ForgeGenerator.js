import { createMercuryTemplate } from "./ForgeTemplates.js";
import { DerivedFieldService } from "./DerivedFieldService.js";

import { AtlasProductBuilder } from "./AtlasProductBuilder.js";
import { ForgeIdGenerator } from "./ForgeIdGenerator.js";
import { ForgeValidator } from "./ForgeValidator.js";
import {PublicationReadinessEngine} from "./PublicationReadinessEngine.js";


export class ForgeGenerator {
    constructor() {
        this.idGenerator = new ForgeIdGenerator();
        this.validator = new ForgeValidator();
        this.atlasBuilder = new AtlasProductBuilder();

        this.derivedFieldService =
        new DerivedFieldService();

        this.publicationReadinessEngine =
            new PublicationReadinessEngine();
    }

    generateProduct(input) {
    const derivedInput =
        this.derivedFieldService.apply(input);

    const validation =
        this.validator.validateInput(
            derivedInput
        );

    const publication =
        this.publicationReadinessEngine.evaluate(
            validation
        );

    if (
        publication.status ===
        PublicationReadinessEngine.STATUS.BLOCKED
    ) {
        return this.createFailedResult({
            validation,
            publication
        });
    }

    const now = new Date().toISOString();
    const dateOnly = now.slice(0, 10);

    const productId =
        this.idGenerator.generateProductId(
            derivedInput.category,
            derivedInput.subcategory,
            derivedInput.productNumber
        );

    const observationId =
        this.idGenerator.generatePriceObservationId(
            dateOnly,
            derivedInput.observationNumber
        );

    const atlasProduct =
        this.atlasBuilder.build({
            input: derivedInput,
            productId,
            timestamp: now,
            validation,
            publication
        });

    const legacyAtlasProduct =
        this.atlasBuilder.buildLegacy({
            input: derivedInput,
            productId,
            timestamp: now
        });

    const mercuryObservation =
        this.createMercuryObservation({
            input: derivedInput,
            productId,
            observationId,
            timestamp: now
        });

    const slug =
        this.createSlug(
            derivedInput.productName
        );

    return {
        atlasProduct,
        legacyAtlasProduct,
        mercuryObservation,

        atlasFilename:
            `${productId}-${slug}.json`,

        mercuryFilename:
            `${observationId}.json`,

        validation,
        publication,

        warnings:
            validation.warnings
    };
}

    createMercuryObservation({
        input,
        productId,
        observationId,
        timestamp
    }) {
        const mercuryObservation =
            createMercuryTemplate();

        mercuryObservation.id = observationId;
        mercuryObservation.productId = productId;
        mercuryObservation.retailerId =
            input.retailerId;
        mercuryObservation.observedAt = timestamp;
        mercuryObservation.price = input.price;
        mercuryObservation.currency = input.currency;
        mercuryObservation.availability =
            input.availability;
        mercuryObservation.condition =
            input.condition;
        mercuryObservation.sellerType =
            input.sellerType;
        mercuryObservation.sourceUrl =
            input.retailerUrl;

        mercuryObservation.verification.verifiedAt =
            timestamp;

        mercuryObservation.verification.notes =
            "Generated by Forge from manually reviewed input.";

        mercuryObservation.metadata.createdAt =
            timestamp;

        mercuryObservation.metadata.updatedAt =
            timestamp;

        mercuryObservation.metadata.notes =
            "Generated by Hardware Radar Forge v0.1.";

        return mercuryObservation;
    }

    createFailedResult({
        validation,
        publication
    }) {
        return {
            atlasProduct: null,
            legacyAtlasProduct: null,
            mercuryObservation: null,
            atlasFilename: null,
            mercuryFilename: null,

            validation,
            publication,

            warnings:
                validation?.warnings ?? []
        };
    }

    createSlug(value) {
        return String(value)
            .trim()
            .toLowerCase()
            .replaceAll(/[^a-z0-9]+/g, "-")
            .replaceAll(/^-+|-+$/g, "");
    }
}