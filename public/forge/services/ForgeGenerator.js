import {
    createAtlasTemplate,
    createMercuryTemplate
} from "./ForgeTemplates.js";

import { ForgeIdGenerator } from "./ForgeIdGenerator.js";
import { ForgeValidator } from "./ForgeValidator.js";

export class ForgeGenerator {
    constructor() {
        this.idGenerator = new ForgeIdGenerator();
        this.validator = new ForgeValidator();
    }

    generateProduct(input) {
        const validation = this.validator.validateInput(input);

        if (!validation.valid) {
            return {
                atlasProduct: null,
                mercuryObservation: null,
                atlasFilename: null,
                mercuryFilename: null,
                validation,
                warnings: validation.warnings
            };
        }

        const now = new Date().toISOString();
        const dateOnly = now.slice(0, 10);

        const productId = this.idGenerator.generateProductId(
            input.category,
            input.subcategory,
            input.productNumber
        );

        const observationId =
            this.idGenerator.generatePriceObservationId(
                dateOnly,
                input.observationNumber
            );

        const atlasProduct = createAtlasTemplate();

        atlasProduct.id = productId;
        atlasProduct.category = input.category;
        atlasProduct.subcategory = input.subcategory;
        atlasProduct.brandId = input.brandId;
        atlasProduct.name = input.productName;
        atlasProduct.model = input.manufacturerPartNumber;
        atlasProduct.manufacturerPartNumber =
            input.manufacturerPartNumber;
        atlasProduct.officialProductUrl = input.manufacturerUrl;

        atlasProduct.metadata.createdAt = now;
        atlasProduct.metadata.updatedAt = now;
        atlasProduct.metadata.sourceReferences = [
            input.manufacturerUrl,
            input.retailerUrl
        ];

        const mercuryObservation = createMercuryTemplate();

        mercuryObservation.id = observationId;
        mercuryObservation.productId = productId;
        mercuryObservation.retailerId = input.retailerId;
        mercuryObservation.observedAt = now;
        mercuryObservation.price = input.price;
        mercuryObservation.currency = input.currency;
        mercuryObservation.availability = input.availability;
        mercuryObservation.condition = input.condition;
        mercuryObservation.sellerType = input.sellerType;
        mercuryObservation.sourceUrl = input.retailerUrl;

        mercuryObservation.verification.verifiedAt = now;
        mercuryObservation.verification.notes =
            "Generated by Forge from manually reviewed input.";

        mercuryObservation.metadata.createdAt = now;
        mercuryObservation.metadata.updatedAt = now;
        mercuryObservation.metadata.notes =
            "Generated by Hardware Radar Forge v0.1.";

        const slug = this.createSlug(input.productName);

        return {
            atlasProduct,
            mercuryObservation,

            atlasFilename: `${productId}-${slug}.json`,
            mercuryFilename: `${observationId}.json`,

            validation,
            warnings: validation.warnings
        };
    }

    createSlug(value) {
        return String(value)
            .trim()
            .toLowerCase()
            .replaceAll(/[^a-z0-9]+/g, "-")
            .replaceAll(/^-+|-+$/g, "");
    }
}